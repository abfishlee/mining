# 엑셀(Excel)과 비슷하게 표 형태로 데이터를 볼 수 있게 해줍니다.

import pandas as pd

# ------------------------------------------------------------------------------
# 1. 데이터 불러오기 (장보기)
# ------------------------------------------------------------------------------
# 판다스의 read_csv() 함수를 사용하면 컴퓨터에 저장된 csv 파일(콤마로 구분된 텍스트)을
# 거대한 '데이터프레임(DataFrame)'이라는 도마 위로 가져올 수 있습니다.
print("--- [1] 데이터 장보기 ---")
df = pd.read_csv('../data/titanic.csv')

# 데이터의 맨 위 5개만 슬쩍 맛보기 (어떤 재료가 있는지 확인)
print("\n데이터 위쪽 5줄 맛보기:")
print(df.head())

# ------------------------------------------------------------------------------
# 2. 데이터 상태 확인 (재료의 신선도 체크)
# ------------------------------------------------------------------------------
print("\n--- [2] 재료 신선도 체크 (info) ---")
# info()는 데이터에 총 몇 개의 줄(행)이 있고, 빈칸(결측치)은 없는지,
# 데이터 타입(숫자인지 문자인지)을 한눈에 보여줍니다.
df.info()

print("\n--- [3] 결측치(썩은 부분) 개수 확인 ---")
# isnull()은 빈칸이면 True를 반환하고, sum()을 하면 True(1)의 개수를 셉니다.
print(df.isnull().sum())

# ------------------------------------------------------------------------------
# 3. 결측치 처리 (썩은 채소 잘라내기 또는 새살 채우기)
# ------------------------------------------------------------------------------
# Age(나이) 열에 빈칸이 많습니다. 이 빈칸을 나이의 '평균' 값으로 채워봅시다.
# fillna() 함수를 사용합니다.
mean_age = df['Age'].mean()  # 나이 평균 계산
df['Age'] = df['Age'].fillna(mean_age)

print(f"\n나이 평균({mean_age:.2f}세)으로 빈칸 채우기 완료! 남은 빈칸 수:")
print(df['Age'].isnull().sum())

